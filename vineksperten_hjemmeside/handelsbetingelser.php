4. Levering

Vi sender til hele Danmark. Vi leverer ikke til udlandet og ikke til Færøerne og Grønland.

Varer vil blive leveret på leveringsadressen, der angives ved bestillingen.
Der leveres varer alle ugens dage. Din vare vil blive leveret 1-3 hverdage efter bestillingen. For visse varer gælder særlige leveringsvilkår. Betingelserne vil fremgå specifikt forud for køb af disse varer.

Separate leveringer

Består din bestilling af flere varer, forbeholder vi os retten til at foretage separate leveringer. Separate leveringer kan være nødvendige, hvis visse varer er forsinkede eller ikke er på lager på bestillingstidspunktet eller såfremt de pågældende varer lagerføres på og afsendes fra forskellige adresser. Du vil forud for købet blive informeret om, at din bestilling er undergivet separate leveringer. Du vil ikke blive opkrævet ekstra leveringsomkostninger ved separate leveringer.

Ved forsendelse via Post Nord:

PRIVATPAKKER MED OMDELING
Der skal kvitteres for pakken ved modtagelsen. Det behøver nødvendigvis ikke være dig der kvitterer. Personer på leveringsadressen (fx et familiemedlem, en ven, receptionist etc.) kan således også kvittere for modtagelsen med bindende virkning for dig. Er der ingen til stede på leveringsadressen, når pakkebuddet kommer, lægges en advisering om, at pakken kan afhentes den efterfølgende dag på nærmeste udleveringssted – med mindre du er tilmeldt Modtagerflex. I sidstnævnte tilfælde stilles forsendelsen på et aftalt sted på adressen. Forsendelsen vil typisk blive opbevaret i maksimalt 14 dage før pakken returneres til os, som ikke-leveret. Husk at du via tilsendte T&T på e-mail kan følge forsendelsens forløb. Såfremt din pakke kan leveres i din postkasse, må pakkebuddet aflevere i denne, uden at indhente underskrift.

PRIVATPAKKER UDEN OMDELING
Er der valgt et afhentningssted sker leveringen til valgte postbutik, posthuset eller til Pakkeboks. Post Nord giver dig besked pr. sms eller e-mail, når pakken er på vej og klar til afhentning. Forsendelsen udleveres mod fremvisning af gyldig legitimation. Har du valgt levering til pakkeboks, bliver koden ligeledes tilsendt. Forsendelsen vil typisk blive opbevaret i maksimalt 14 dage før pakken returneres til os, som ikke leveret. Husk at du via tilsendte T&T på e-mail kan følge forsendelsens forløb.

