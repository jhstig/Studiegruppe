5. Fortrydelsesret (gælder kun for forbrugere)

Magasin giver dig ret til at fortryde en aftale uden begrundelse inden for 30 dage.

Fortrydelsesfristen udløber 30 dage efter den dag, hvor du eller en af dig angiven tredjemand, dog ikke transportøren, får varerne i fysisk besiddelse.

I tilfælde hvor flere forskellige varer er bestilt i én ordre, men leveres enkeltvis (dvs. separate leveringer), udløber fortrydelsesfristen 30 dage efter den dag, hvor du eller en af dig angivet tredjemand, dog ikke transportøren, får den sidste vare i fysisk besiddelse.

Det er ikke muligt at fortryde købet ved blot at nægte modtagelse af varen, se nærmere i afsnittet om returnering.

Fortrydelsesfristen er overholdt, hvis du sender din meddelelse om udøvelse af fortrydelsesretten, inden fortrydelsesfristen er udløbet.

Varer undtaget fortrydelsesretten

Følgende varetyper er undtaget fortrydelsesretten:

 Levering af varer, som er fremstillet efter forbrugerens specifikationer eller har fået et tydeligt personligt præg,
 Levering af forseglede varer, som af sundhedsbeskyttelses- eller hygiejnemæssige årsager ikke er egnet til at blive returneret, og hvor forseglingen er blevet brudt efter leveringen,
Du mister din fortrydelsesret, hvis du bryder forseglingen på varer, som af sundhedsbeskyttelses- eller hygiejnemæssige årssager ikke er egnet til at blive returneret.

