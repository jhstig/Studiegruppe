Såfremt du skulle have nogle spørgsmål til eller er utilfreds med varen du har købt, kan du kontakte Magasin direkte på online@magasin.dk, hvor vi vil forsøge at afhjælpe problemet.

Hvis du er utilfreds med varen, du har købt og ikke kan blive enig med vores Kundeservice om en fornuftig løsning på tvisten, har Du adgang til at klage til Forbrugerklagenævnet. Du kan følge linket for at indgive en online klage hos Forbrugerklagenævnet.

Alternativt kan du rette skriftlig henvendelse til:
