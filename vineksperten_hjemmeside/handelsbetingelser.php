Det er ikke muligt at vælge alternativ leveringsmetode. Levering sker i hverdage mellem kl. 8-16. Du modtager et opkald senest 30 minutter før levering. Møbler leveres ved kantstenen. Det er ikke muligt at bestille levering med indbæring.

Bemærk venligst, at du bærer risikoen for varen efter leveringen. Ser emballagen ud til at være skadet, bør du afvise at modtage varen. Ønsker du at klage over eventuelle fejl og mangler skal du informere os om det, læs mere under "Defekter varer og fejl-leverancer"

